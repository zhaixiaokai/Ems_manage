package com.sohu.edm.model;


public class MailInfoDomain {
	private String mailName;
	private String mailSubject;
	private String sendTime;
	private String sendUser;
	private String ent;
	private String org;
	private String sendCount;
	private String payPoint;
	private String retPoint;
	private String actPoint;
	public String getMailName() {
		return mailName;
	}
	public void setMailName(String mailName) {
		this.mailName = mailName;
	}
	public String getMailSubject() {
		return mailSubject;
	}
	public void setMailSubject(String mailSubject) {
		this.mailSubject = mailSubject;
	}

	public String getSendTime() {
		return sendTime;
	}
	public void setSendTime(String sendTime) {
		this.sendTime = sendTime;
	}

	public String getSendUser() {
		return sendUser;
	}
	public void setSendUser(String sendUser) {
		this.sendUser = sendUser;
	}
	public String getEnt() {
		return ent;
	}
	public void setEnt(String ent) {
		this.ent = ent;
	}
	public String getOrg() {
		return org;
	}
	public void setOrg(String org) {
		this.org = org;
	}
	public String getSendCount() {
		return sendCount;
	}
	public void setSendCount(String sendCount) {
		this.sendCount = sendCount;
	}
	public String getPayPoint() {
		return payPoint;
	}
	public void setPayPoint(String payPoint) {
		this.payPoint = payPoint;
	}
	public String getRetPoint() {
		return retPoint;
	}
	public void setRetPoint(String retPoint) {
		this.retPoint = retPoint;
	}
	public String getActPoint() {
		return actPoint;
	}
	public void setActPoint(String actPoint) {
		this.actPoint = actPoint;
	}

}
